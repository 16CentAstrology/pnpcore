namespace PnP.Core.Model.SharePoint
{
    /// <summary>
    /// This enum contains all the extensions that are allowed for a file to be converted to a PDF using MS Graph
    /// </summary>
    public enum AllowedConvertExtensions
    {
        /// <summary>
        /// Comma Separated Values
        /// </summary>
        csv,
        /// <summary>
        /// Microsoft Word 97-2003 processing File
        /// </summary>
        doc,
        /// <summary>
        /// Microsoft Word processing File
        /// </summary>
        docx,
        /// <summary>
        /// OpenDocument Presentation File
        /// </summary>
        odp,
        /// <summary>
        /// OpenDocument Spreadsheet File
        /// </summary>
        ods,
        /// <summary>
        /// OpenDocument Document File
        /// </summary>
        odt,
        /// <summary>
        /// Microsoft PowerPoint Template File
        /// </summary>
        pot,
        /// <summary>
        /// Microsoft PowerPoint with macro's enable Template File
        /// </summary>
        potm,
        /// <summary>
        /// Microsoft PowerPoint Template File
        /// </summary>
        potx,
        /// <summary>
        /// Microsoft PowerPoint 97-2003 Slide Show File
        /// </summary>
        pps,
        /// <summary>
        /// Microsoft PowerPoint Slide Show File
        /// </summary>
        ppsx,
        /// <summary>
        /// Microsoft PowerPoint 97-2003 with macro's enabled File
        /// </summary>
        ppsxm,
        /// <summary>
        /// Microsoft PowerPoint 97-2003 Presentation File
        /// </summary>
        ppt,
        /// <summary>
        /// Microsoft PowerPoint with macro's enabled File
        /// </summary>
        pptm,
        /// <summary>
        /// Microsoft PowerPoint Presentation File
        /// </summary>
        pptx,
        /// <summary>
        /// Rich Text Format File
        /// </summary>
        rtf,
        /// <summary>
        /// Microosft 97-2003 Excel Spreadsheet file
        /// </summary>
        xls,
        /// <summary>
        /// Microosft Excel Spreadsheet File
        /// </summary>
        xlsx
    }
}
